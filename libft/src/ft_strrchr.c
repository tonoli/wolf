#include "../inc/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = ft_strlen(s);
	str = (char *)s;
	while (i != 0 && str[i] != c)
		i--;
	if (str[i] == c)
		return (&str[i]);
	return (NULL);
}
